public class Array
{
	public static void main(String[] args)
	{
		//declaring array
		int anArray[];

		//assigning memory
		anArray = new int[3];

		anArray[0] = 13;
		anArray[1] = 26;
		anArray[2] = 39;

		System.out.println("Number 1 : " + anArray[0]);
		System.out.println("Number 2 : " + anArray[1]);
		System.out.println("Number 3 : " + anArray[2]);
	}
}