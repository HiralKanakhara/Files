public class One_dimensional_array
{
	public static void main(String[] args)
	{
		int[] arr = new int[4];
		arr[0] = 4;
		arr[1] = 8;
		arr[2] = 12;
		arr[3] = 16;

		System.out.println(arr[0]);
		System.out.println(arr[1]);
		System.out.println(arr[2]);
		System.out.println(arr[3]);
	}
}